package LektionerWithAdam.Lektion4;

public class IfElse {
    public static void main(String[] args) {

        int num = 5;
        if (num <= 5) {
            System.out.println("number is less than 5 ");

        } else if (num >= 5) {
            System.out.println("Number is not less or equal than 5 ");
        } else {
            System.out.println("Something strange happened! ");
        }


    }
}
